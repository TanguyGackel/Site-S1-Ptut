            <h2>I) Les moteurs de jeux, un marché encore en développement</h2>
 
            <h3>a) Les recettes des moteurs de jeux</h3>
            
            <p>
            Le domaine des jeux vidéo est de plus en plus présent au niveau économique.
            En effet, en France, il a représenté à lui seul presque 5 milliards d’euros pour l’année 2019.
            Unity, un moteur de jeux qui est présent à la fois dans les jeux sur pc, console mais également sur mobile. C’est l’un des plus puissants du marché du jeu vidéo. Son capital s’élève à plus de 6 milliards d’euros (en 2019). Epic Games, l’entreprise possédant le moteur de jeu Unreal Engine, est le plus grand rival de Unity. Cette entreprise a amassé une somme considérable grâce au jeu Fortnite (créé par le moteur de l’entreprise). Ce succès a rapporté plus de 2,4 milliards de dollars à l’entreprise ,rien qu’en 2018.
            Cette année, l’arrivée de la Covid n’a pas eu de répercussion dans ce secteur. En effet, durant le confinement une légère hausse s’est produite. Cela a été profitable pour les moteurs de jeux tel le français Ubisoft, qui a réalisé une augmentation de 17,6 % de son chiffre d’affaire, soit environ 400 millions d’euros. 
            <p>    
            
            <h3>b) L’entrée en bourse d’Unity</h3>
            
            </p>
            Unity Technologies est un leader dans le domaine des moteurs de jeux. Il était au départ concentré sur le domaine du jeu vidéo. Actuellement, il s’est développé et est présent dans des projets dans divers domaines tels que l’architecture et l’ingénierie ou bien encore dans le domaine automobile. Ses revenus ont augmenté ces dernières années et les pertes sont en baisse, ce sont des tendances convenables pour d’éventuelles entreprises qui souhaiteraient investir.
            Comme ces tendances étaient plus qu’encourageantes, Unity a souhaité s’introduire en bourse.
            Depuis septembre 2020, il fait désormais partie de la bourse de New York.
            Le nombre d’actions mises en vente était de 263 366 733 actions.
            Les principaux actionnaires de Unity sont Sequoia (21,8 %), Silver Lake (16,4) ainsi que certains dirigeants de l’entreprise.
            Les résultat sont en faveur de l’entreprise car dès son entrée en bourse, le prix de l’action était 52$ l’unité. Actuellement le cours est presque 3 fois plus important, car le prix de l’action est de 130$.
            Unity compte aujourd’hui un capital boursier qui s’élève à 35 milliards de dollars.
            </p>

            <h2>II) Les différents modèles économiques</h2>
            
            <h3>a) Le modèle classique, par abonnement (Unity)</h3>
            
            <p>
            Unity a su s’adapter et innover afin de satisfaire un maximum de personnes et a opté pour un modèle économique par abonnement. Il propose 3 offres. La première, Unity Personal, disposant des éléments de base du développement est gratuite et est destinée pour des développeurs débutants. Les autres offres, Unity Plus et Unity Pro disposent de fonctions supplémentaires et sont destinées à des développeurs ayant déjà une expérience ainsi qu’un certain revenu.
            Cela fait de Unity un des leaders dans le monde du jeu vidéo car il a permis une importante hausse des développeurs dans ce milieu.
            </p>
                
            <h3>b) Le modèle par royalties (Unreal Engine)</h3>
            
            <p>
            Le moteur Unreal Engine propose aux développeurs une utilisation gratuite de ses outils.
            Mais cela n’est valable que pour les « petits » développeurs. En effet, au-delà de 1 million de dollars générés à partir des revenus d’un jeu créé sur cette plateforme, le développeur doit verser une commission à Epic Games, l’entreprise du moteur. Cette commission est définie à 5 % du chiffre d’affaires du jeu.
            Ce modèle économique est dit par royalties. C’est ce que critique John Riccitiello, l’actuel PDG de Unity, en dénonçant ce modèle comme étant « le pire qui soit ».
            </p>
                
            <h3>c) La gratuité totale du moteur de jeu (Amazon)</h3>
            
            <p>
            Amazon, grande enseigne qui s’occupe principalement du domaine de la distribution a décidé de se développer.
            L’entreprise s’est peu à peu dirigée dans le domaine du jeu vidéo. Elle a depuis 2016 proposé son propre moteur de jeu nommé Lumberyard. Il a la particularité d’être entièrement gratuit. Cela permet à tous les développeurs de s’en servir sans aucune restriction. Le moteur de jeu a déjà fait ses preuves car il a été utilisé pour quelques gros projets comme le jeu Star Citizen ou encore Crucible.
            </p>

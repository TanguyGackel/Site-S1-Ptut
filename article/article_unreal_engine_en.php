        <h3>I) The main screen</h3>
        <figure>
            <img src="img/main_screen.PNG" alt="main_screen_unreal">
            <figcaption>Screenshot: Main Screen Unreal Engine [GACKEL Tanguy]</figcaption>
        </figure>
        <p>This is the main screen: this is where you can add content and access the
            rest of the software.
        </p>
        <ol>
            <li>The "File" button is similar to that of other software, opening projects, the
                save, import or export them... The "Edit" button allows you to modify the
                project parameters, and the "Window" button allows you to manage the different menus. It
                It's better not to touch it when you're new to the software. Finally, the "Help" button
                refers to various documentation and tutorials.</li>
            <li>
                Here, for a beginner, there are 3 buttons to know: "Save Current" which allows to
                make a quick backup of the project, "Build" which allows to update (among other things)
                light and shadow effects, and "PLAY", which allows you to test the project.
            </li>
            <li>
                This window allows you to see the content of the project, where the different
                objects, but also to add, delete, modify them ...
            </li>
            <li>
                The "Place Actor" menu allows you to quickly add basic objects, such as
                geometrical shapes (sphere, cube, cone ...), lighting of different shapes, etc...
                types, or cameras (which represent the point of view of the player(s)).
            </li>
            <li>
                The "Content Browser" allows you to view and add "assets" to your project 3
                textures, 3D models such as those obtained in the marketplace or those created
                by the user
            </li>
            <li>
                The "World Outliner" menu allows to view and select any item
                present on the stage.
            </li>
            <li>
                The "Details" menu allows you to quickly modify certain features
                basic information about the selected object, such as its position, if it is a dynamic object or
                no, the texture to be applied to it, or the way it handles lighting.
            </li>
        </ol>
    </article>
    <p>This is a simplified summary, for a quick introduction to the software. <br>

        <h3>I) L’écran principal</h3>
        <figure>
            <img src="img/main_screen.PNG" alt="main_screen_unreal">
            <figcaption>Screenshot: Main Screen Unreal Engine [GACKEL Tanguy]</figcaption>
        </figure>
        <p>Voici l’écran principal : c’est ici que vous pouvez ajouter du contenu et accéder au
            reste du logiciel.
        </p>
        <ol>
            <li>Le bouton “File” est similaire à celui d’autres logiciels, ouvrir des projets, les
                sauvegarder, les importer ou les exporter… Le bouton “Edit” permet de modifier les
                paramètres du projet, et le bouton “Window” permet de gérer les différents menus. Il
                ne vaut mieux pas y toucher quand on débute sur le logiciel. Enfin, le bouton “Help”
                renvoie vers diverses documentations et tutoriels.</li>
            <li>
                Ici, pour un débutant, il y a 3 boutons à connaître : “Save Current” qui permet de
                faire une sauvegarde rapide du projet, “Build” qui permet d’actualiser (entre autres)
                les effets de lumières et d’ombres, et “PLAY”, qui permet de tester le projet.
            </li>
            <li>
                Cette fenêtre permet de voir le contenu du projet, où sont placés les différents
                objets, mais également d’en rajouter, d’en supprimer, de les modifier …
            </li>
            <li>
                Le menu “Place Actor” permet de rajouter rapidement des objets basiques, tels
                que des formes géométriques (sphère, cube, cône …), des éclairages de différents
                types, ou encore des caméras (qui représentent le point de vue du/des joueur(s)).
            </li>
            <li>
                Le “Content Browser” permet de voir et d’ajouter à son projet des ”assets” 3
                , des textures, des modèles 3D tels que ceux obtenus dans le marketplace ou ceux créés
                par l’utilisateur
            </li>
            <li>
                Le menu “World Outliner” permet de voir et de sélectionner n’importe quel élément
                présent sur la scène.
            </li>
            <li>
                Le menu “Details” permet de modifier rapidement certaines caractéristiques
                basiques de l’objet sélectionné, tel que sa position, s’il s'agit d’un objet dynamique ou
                non, la texture à lui appliquer, ou encore sa façon de gérer l’éclairage.
            </li>
        </ol>
    </article>
    <p>Il s'agit la d'un résumé simplifié, pour une rapide prise en main du logiciel. <br>
        Expliquer toutes les fonctionnalité ainsi que tous les menus du logiciels prendrait des dizaines d'heures.</p>

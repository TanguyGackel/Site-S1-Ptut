        <h2>L’exemple d’un moteur de jeu : l’Unreal Engine</h2>
        <em>GACKEL Tanguy</em>
        <p>Pour mieux comprendre comment fonctionnent et à quoi servent les moteurs
            de jeux, il est important de savoir à quoi ils ressemblent.</p>
        <p>
            L’installation de l’Unreal Engine se fait par l’Epic Games Store. Depuis le
            launcher, on peut gérer les différents projets, en apprendre plus sur l’utilisation de
            l’Unreal Engine, ou encore accéder au marketplace. Le marketplace permet d’obtenir
            des modèles 3D, des textures, des animations, des scripts fait par d’autres
            utilisateurs ou directement par Epic Games. Gratuit ou payant, ils seront ensuite
            utilisables directement dans l’Unreal Engine.
        </p>

    <h2>I) Qu’est-ce qu’un moteur de jeu ?</h2>
    <em>GAGNE Maxens</em>
    <p>
        Un moteur de jeu, en anglais Game Engine, est un logiciel tiers, une interface, qui regroupe plusieurs fonctionnalités compilées dans un seul programme.
        Par exemple, il peut intégrer : un moteur 3D, un moteur 2D, un moteur d’UI, un moteur physique, un moteur d’animation, un gestionnaire des assets, un éditeur de scripts, un gestionnaire des scènes, l’export sur différentes plateformes, etc.
        Le but est d’utiliser tous ces outils existants afin de faire gagner du temps de développement, Mais la création de tous ces moteurs est chronophage et coûte beaucoup d’argent.
        L’entièreté des moteurs de jeux gère de façon efficace les graphiques, l’audio, et la logique.
        Les moteurs de jeux plus modernes sont devenus au fil du temps, plus spécialisés, et incluent désormais des interfaces pour la physique, l’IA, la mise en réseau, l’optimisation etc.
    </p>

    <h2>II)  Histoire et Origine</h2>

    <p>
        Au commencement des jeux vidéos (~1980), chaque jeu était créé de zéro.
        Les developpeurs devaient tout gérer manuellement, que ce soit la mémoire, les
        mouvements, les images, la physique etc. Parlons de la mémoire et des images par
        exemple. Quand ils devaient créer un jeu basique de plateformes en 2D, ils devaient
        gérer uniquement l’arrière-plan, le sol, les objets fixes au sol, les objets à ramasser,
        notre personnage et des ennemis. Tous ces objets sont gérés par la mémoire qui doit
        les afficher aux bonnes coordonnées. La mémoire doit également supprimer ces
        objets une fois non nécessaire pour laisser place aux potentiels prochains objets.
        Avec un jeu fait à partir de zéro, cela doit se faire manuellement pour chaque et
        unique objet. Mais avec un moteur de jeu, le système va pouvoir garder une trace de
        ces objets et gérer la mémoire automatiquement. Au début ce type de
        fonctionnement était fait pour chaque jeu de façon différente mais pour encore plus
        optimiser le temps de développement, des interfaces de gestion générales que sont
        les moteurs de jeux sont nés, simplifiant la création du jeu.
    </p>
    <p>
        Cela parait logique de ne pas repartir de zéro à chaque nouvelle création de
        jeu, car dans chaque jeu il y a une structure à respecter qui est un monde, un
        personnage et des affichages graphiques etc. Le problème de ces moteurs de jeux,
        est qu’ils prenaient plus de mémoire à gérer le jeu, que le jeu en lui même. C’est
        pour cela qu’ils ne sont apparus que très récemment car la mémoire était précieuse
        et limitée. Les moteurs de jeux ont commencés à émerger uniquement à partir du
        lancement de jeux en 3D et lorsque les technologies commençaient à beaucoup plus
        se compliquer. Il reste tout de même aujourd’hui, beaucoup de studios qui créent
        leurs propres moteurs de jeux privés pour mieux convenir à leurs besoins. Pour les
        studios, il faut donc choisir entre passer des milliers d’heures à créer leurs propres
        moteurs de jeu à partir de rien, ou gagner du temps en utilisant/achetant un moteur
        de jeu déjà existant.
    </p>
    <p>
        Les moteurs de jeux d’aujourd’hui permettent de créer des mondes et
        graphiques proches de la réalité et avec un qualité indistinguable. Même si ces
        créations sont impressionnantes, cela n’apparaît pas réellement dans des jeux pour
        le moment, ce sont juste des exemples de ce qu’il serait possible de faire. On
        pourrait donc espérer des mondes et jeux de cette qualité dans un futur non loin,
        notamment avec l’arrivée de nouveaux moteurs de jeux, comme Unreal Engine 5 par
        exemple
    </p>


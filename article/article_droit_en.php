        some young developers do not. Borrowing from banks may be one solution, but there is a
        risk that the project may not be fully completed.
        It is also possible that some people will use existing game parts.
        However, to keep the project to oneself and to avoid that possible copies are made to the detriment of the creator, it is necessary to
        must be able to protect its realization and be able to demonstrate that one is indeed the author of the project.

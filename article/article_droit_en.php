    <h3>b) How to protect games created by developers?</h3>

    <p>
        It may be a good idea to protect a game to avoid any potential risk.
        To do so, you need to own the rights to it.
        Today, there is no legal regime that is entirely dedicated to the field of video games.
        It is a complex work in which each element is subject to the legal regime of the nature to which it is subject.
        it corresponds, whether at the technical or artistic level.
        It is possible to patent one's work but this remains rare in this field.
        In order to protect the important elements of video games, it is possible to call upon different organizations
        such as Ohmi (European Agency for Trade Marks and Designs) and WIPO (World Intellectual Property Organization).
        intellectual property) that are able to achieve protection of a work. For a game to be protected,
        you must therefore own the rights to the game and prove its authenticity.
    </p>

    <h2>II) Game protection</h2>

    <h3>a) Intellectual Property</h3>

    <p>
        Intellectual property includes all the exclusive rights granted on intellectual creations.
        There are various rights that are grouped together at the level of intellectual property.
        Copyrights are used to protect the inventions of people (music, graphics, the
        scenario). Trademark rights are used to protect the use of external elements that are found in
        in the game ( the logos, the title of the game ).
        There is also the right of business secrets which includes the exchanges between the people in charge
        of the project (developer contacts, contract clauses).
        In France, when a game is the result of the work of several people and it is not possible to
        To determine the contributions of each, it will be referred to as a collaborative work, sometimes called a work of art.
        in the gaming sector. The copyright will no longer belong to the designers but to the publisher.
    </p>

    <h3>b) The protection of the elements that make up a video game</h3>

    <p>
        At the level of the video game itself, different elements are able to be protected.
        The software used within the game can be protected by copyright, it is however necessary to demonstrate that
        the intellectual contribution of the creator within the software. It is also necessary that this software is
        materialized, sufficiently elaborated because simple ideas or concepts cannot be the subject of a
        appropriation. What can be protected by copyright at the software level is the source code as follows
        preparatory design material.
        A database can also be protected, whether it is the architecture of the database or
        the content of the latter.
        The name and logo of a video game may be protected by copyright.
        With respect to audiovisual content, since it is generally a collaborative work in
        to which several people have committed themselves, it is the person under whose name the work is disclosed.
        who benefits from the rights.
        Finally, the gameplay, that is to say all the mechanisms allowing to control a character and to decide how he can
        interacting in its environment, can also be protected by copyright.

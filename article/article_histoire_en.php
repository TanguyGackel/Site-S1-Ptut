        <h2>I) What is a game engine ?</h2>
        <em>GAGNE Maxens</em>
        <p>
            A Game Engine is a third party software, an interface, which combines several features compiled into a
            single program.
            For example, it can integrate: a 3D engine, a 2D engine, a UI engine, a physics engine, an animation engine,
            an asset manager, a script editor, a scene manager, export on different platforms, etc.
            The goal is to use all these existing tools in order to save development time, but the creation of all these
            engines is time consuming and costs a lot of money.
            The entire game engine efficiently manages graphics, audio, and logic.
            More modern game engines have become more specialized over time, and now include interfaces for physics, AI,
            networking, optimization, etc.
        </p>

        <h2>II) History and Origin</h2>

        <p>
            At the beginning of video games (~1980), each game was created from scratch.
            Developers had to manage everything manually, from memory, to the
            movements, images, physics etc. Let's talk about memory and images by
            example. When they had to create a basic 2D platform game, they had to
            manage only the background, the ground, fixed objects on the ground, objects to be picked up,
            our character and enemies. All these objects are managed by the memory which must
            display them at the right coordinates. The memory must also delete these
            objects once not necessary to make room for potential next objects.
            With a game made from scratch, this has to be done manually for each and every and
            single object. But with a game engine, the system will be able to keep a trace of
            these objects and manage the memory automatically. At the beginning this type of
            each game in a different way, but for even more, it was done in a different way for each game.
            optimize the development time, general management interfaces such as
            the game engines are born, simplifying the creation of the game.
        </p>
        <p>
            It seems logical not to start from scratch with each new creation of
            game, because in each game there is a structure to be respected which is a world, a
            character and graphic displays etc. The problem with these game engines,
            is that they took more memory to manage the game, than the game itself. This is
            for this reason they appeared only very recently because memory was precious.
            and limited. Game engines started to emerge only from the
            launch of 3D games and when the technologies started to become much more advanced, the
            become more complicated. There are still many studios today that are still creating
            their own private game engines to better suit their needs. For
            studios, you have to choose between spending thousands of hours creating your own studios and
            game engines from scratch, or save time by useing/buying an engine
            already existing game.
        </p>
        <p>
            Today's gaming engines allow to create worlds and
            graphics close to reality and with indistinguishable quality. Even if these
            creations are impressive, this does not really appear in games for the general public.
            At the moment, these are just examples of what could be done. On
            could therefore hope for worlds and games of this quality in the near future,
            notably with the arrival of new game engines, such as Unreal Engine 5 by
            example
        </p>


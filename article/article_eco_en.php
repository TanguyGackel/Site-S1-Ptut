            <h2>I) Game engines, a market still in development</h2>
 
            <h3>a) Revenues from game engines</h3>
            
            <p>
                The field of video games is more and more present at the economic level.
                Indeed, in France alone, it represented almost 5 billion euros for the year 2019.
                Unity, a game engine that is present both in PC, console and mobile games. It is one of the most powerful in the video game market. Its capital amounts to more than 6 billion euros (in 2019). Epic Games, the company that owns the Unreal Engine, is Unity's biggest rival. The company has raised a considerable amount of money thanks to the Fortnite game (created by the company's engine). This success has earned the company more than $2.4 billion in 2018 alone.
                This year, the arrival of Covid has had no impact in this sector. In fact, during containment a slight increase occurred. This was profitable for game engines such as the French company Ubisoft, which achieved a 17.6% increase in sales, or approximately 400 million euros.             <p>
            
            <h3>b) Unity's IPO</h3>
            
            </p>
            Unity Technologies is a leader in the field of game engines. It was initially focused on the video game industry. Currently, it has developed and is present in projects in various fields such as architecture and engineering or in the automotive field. Its revenues have increased in recent years and losses are decreasing, which are suitable trends for potential companies that would like to invest.
            As these trends were more than encouraging, Unity wanted to go public.
            Since September 2020, it has been listed on the New York Stock Exchange.
            The number of shares offered for sale was 263,366,733.
            Unity's major shareholders are Sequoia (21.8%), Silver Lake (16.4), and certain company executives.
            The results are in the company's favor, since the share price was $52 per unit at the time of the IPO. Currently the price is almost 3 times higher, as the share price is $130.
            Unity now has a market capitalization of $35 billion.
            </p>

            <h2>II) The different economic models</h2>
            
            <h3>a) The classic model, by subscription (Unity)</h3>
            
            <p>
                Unity has been able to adapt and innovate in order to satisfy a maximum number of people and has opted for a subscription-based business model. It proposes 3 offers. The first one, Unity Personal, with the basic elements of development is free of charge and is intended for beginning developers. The other offers, Unity Plus and Unity Pro, have additional features and are intended for developers who already have experience and a certain income.
                This makes Unity one of the leaders in the world of video games, as it has enabled a significant increase in the number of developers in this field.
            </p>
                
            <h3>b) The royalty model (Unreal Engine)</h3>
            
            <p>
                The Unreal Engine offers developers free use of its tools.
                But this is only valid for "small" developers. Indeed, over 1 million dollars generated from the revenues of a game created on this platform, the developer must pay a commission to Epic Games, the engine company. This commission is set at 5% of the turnover of the game.
                This business model is called royalty-based. This is what John Riccitiello, the current CEO of Unity, criticizes, denouncing this model as "the worst there is".            </p>
                
            <h3>c) Totally free game engine (Amazon)</h3>
            
            <p>
                Amazon, a major retailer that deals mainly with distribution, has decided to expand.
                The company gradually moved into the video game field. Since 2016 it has offered its own game engine called Lumberyard. It has the particularity of being entirely free. This allows all developers to use it without any restriction. The game engine has already proven itself as it has been used for some big projects like the game Star Citizen or Crucible.
            </p>

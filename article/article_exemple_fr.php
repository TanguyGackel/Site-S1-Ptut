        <h2>Présentation de jeux et projets sous Unreal Engine</h2>
        <em>GACKEL Tanguy</em>
        <p>De nombreuses entreprises utilisent le moteur de jeu créer par Epic Games,
        l'Unreal Engine 4. Ces jeux peuvent être de tout types, mais sont généralement des
        jeux en 3 dimensions, car l'Unreal Engine a été particulièrement optimisé pour ça.</p>
    </article>
    <article>
        <h3>I) Jeux réalisé avec l’Unreal Engine 4</h3>
        <h4>1) Fortnite Battle Royale (2017):</h4>
        <p>Fortnite est un jeu développé et édité par Epic Games avec l'Unreal Engine.
            Il s'agit d'un Battle Royal, c'est à dire un jeu ou tous les joueurs sont lâchés dans une
            arène dont les bords se réduisent au fur et à mesure de la partie, obligeant les
            joueurs à se rencontrer, et donc se battre. Le dernier survivant est le vainqueur</p>
        <p>Le fait de créer un jeu aussi ambitieux avec leurs propre moteur de jeu a
            permis à Epic Games de se rendre compte des défauts de ce dernier, ils en ont donc
            profité pour l'optimiser, notamment au niveau des performances serveurs, mais aussi
            pour l'affichage des objets lointains, nécessaire dans un jeu de tir.</p>
        <br/>
        <h4>2) Ark: Survival Evolved (2015)</h4>
        <p>Ark est un jeu de survie bac à sable édité par le Studio Wildcard avec
            l'Unreal Engine. Dans ce jeu, le joueur se retrouve sur une île peuplée de
            dinosaures, et doit survivre tout en se fabricant des armes, des abris, et ainsi
            débloquer de nouvelles technologies. Le jeu met l'accent sur un monde vaste et des
            graphismes réalistes, malgré la présence de nombreux bugs.</p>
        <p>Ark bénéficie de nombreux “mods“
            créés par la communauté, et une
            extension de l'Unreal Engine a spécialement été créé pour cela. L'Ark Dev Kit permet
            ainsi de créer des “mods“ de façon simplifié en utilisant une version modifiée de
            l'Unreal Engine, même pour des personnes avec un faible niveau en programmation.</p>
        <br/>
        <h4>3) Projet BLARI (2020)</h4>
        <p>Projet BLARI est un jeu développé par le youtubeur MaxEstLa. Il s'agit de
            son deuxième jeu, le premier ayant également été fait avec l'Unreal Engine. Il a été
            aidé la musique et les sons d’ambiance. Projet BLARI est un jeu en 3 dimensions fait
            dans le style des vieux jeux sur la console PS1.</p>
        <p>Ce jeu montre bien que n'importe qui peut faire un jeu avec l'Unreal Engine,
            même un indépendant, seul, et que l'apprentissage du logiciel est assez rapide
            (MaxEstLa a appris une grande partie des fonctionnalités pendant qu'il développait
            son jeu).</p>
    </article>
    <article>
        <h3>II) Projets fait avec Unreal Engine 4</h3>
        <h4>1) The Mandalorian (2020)</h4>
        <p>L'Unreal Engine n'est pas seulement utilisé pour créer des jeux vidéo. Grâce
            aux graphismes ultra-réalistes permis par ce moteur de jeux, son utilisation peut
            également être détournée et utilisée à d'autre fin.</p>
        <p>The Mandalorian est une série télévisée se déroulant dans l'univers de Star
            Wars. La particularité de The Mandalorian est que les fonds verts n'ont pas ou très
            peu été utilisés. A la place, les scènes étaient tournées dans des pièces où les murs
            sont composés de gigantesques panneaux LED. Les différentes scènes ont été
            modélisé avec Unreal Engine 4, puis affiché sur les écrans. Cette méthode possède
            de nombreux avantages, notamment que le résultat est directement visible par les
            acteurs et le réalisateur. Des modifications du décors peuvent donc être effectué en plein pendant le tournage.</p>

    <h2>I) Des dispositions juridiques appropriées pour assurer le bon développement des jeux</h2>

    <h3>a) Quels sont les risques des développeurs?</h3>

    <p>
        Les développeurs de jeux vidéos sont de plus en plus nombreux en ce moment .
        Les projets de jeux sont eux aussi très nombreux et pour que cela aboutisse , il faut des moyens financiers que
        certains jeunes développeurs n’ont pas. L’emprunt auprès des banques peut-être une solution mais il y a un
        risque que le projet n’aboutisse pas totalement.
        Il est aussi possible que certaines personnes se servent des parties de jeux existant.
        Or pour garder son projet pour soi et éviter que d’éventuelles copies soient faites au détriment du créateur, il
        faut pouvoir protéger sa réalisation et être en capacité de démontrer que l’on est bien l’auteur du projet.
    </p>

    <h3>b) Comment protéger les jeux créés par les développeurs?</h3>

    <p>
        Il peut être bien de protéger un jeu afin d’éviter tout risque potentiel.
        Pour ce faire, il faut en posséder les droits.
        Aujourd’hui, il n’y a pas de régime juridique qui soit entièrement dédié au domaine du jeu vidéo.
        Il s’agit d’une œuvre complexe dans laquelle chaque élément est soumis au régime juridique de la nature auquel
        il correspond, que ce soit au niveau technique ou au niveau artistique.
        Il est possible de breveter son œuvre mais cela reste rare dans ce milieu.
        Pour protéger les éléments importants des jeux vidéos, il est possible de faire appel à différents organismes
        comme l’Ohmi (Agence européenne pour les marques, dessins et modèles) et l’Ompi (Organisation mondiale de la
        propriété intellectuelle) qui sont en mesure de réaliser la protection d’une œuvre. Pour qu’un jeu soit protégé,
        il faut donc posséder les droits du jeu et prouver de son authenticité.
    </p>

    <h2>II) La protection des jeux</h2>

    <h3>a) La propriété intellectuelle</h3>

    <p>
        La propriété intellectuelle regroupe l’ensemble des droits exclusifs accordés sur des créations intellectuelles.
        Il existe différents droits qui sont regroupés au niveau de la propriété intellectuelle.
        Les droits d’auteurs sont utilisés pour protéger les inventions de personnes (la musique, le graphisme, le
        scénario). Les droits des marques sont utilisés pour protéger l’utilisation d’éléments extérieur qui se trouvent
        dans le jeu ( les logos, le titre du jeu ).
        On trouve aussi le droit des secrets d’affaire qui regroupent les échanges effectués entre personnes en charge
        du projet ( contacts des développeurs, clauses du contrat).
        En France, lorsqu’un jeu est le fruit du travail de plusieurs personnes et qu’il n’est pas possible de
        déterminer les contributions de chacun, il sera qualifié d’œuvre de collaboration, parfois appelé œuvre
        collective dans le secteur du jeu. Les droits d’auteurs ne reviendront plus aux concepteurs mais à l’éditeur.
    </p>

    <h3>b) La protection des éléments qui composent un jeu vidéo</h3>

    <p>
        Au niveau du jeu vidéo en lui même , différents éléments sont en mesure d’être protégés.
        Le logiciel utilisé au sein du jeu peut être protégé par le droit d’auteur, il faut toute fois démontrer
        l’apport intellectuel du créateur au sein du logiciel. Il est également nécessaire que ce logiciel soit
        matérialisé, suffisamment élaboré car les simples idées ou les concepts ne peuvent pas faire l’objet d’une
        appropriation. Ce qui peut être protégé par le droit d’auteur au niveau logiciel, ce sont le code source ainsi
        que le matériel de conception préparatoire.
        Une base de données peut faire également l’objet d’une protection, que ce soit l’architecture de la base ou bien
        le contenu de celle-ci.
        Le nom et le logo d’un jeu vidéo peuvent être protégés par le droit d’auteur.
        En ce qui concerne le contenu audiovisuel, puisqu’il s’agit généralement d’une œuvre de collaboration dans
        laquelle plusieurs personnes se sont investies, c’est la personne sous le nom de laquelle l’œuvre est divulguée
        qui bénéficie des droits.
        Enfin, le gameplay, soit tout les mécanismes permettant de contrôler un personnage et de décider comment il peut
        interagir dans son environnement, peut lui aussi être protégé par le droit d’auteur.
    </p>

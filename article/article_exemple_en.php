        <h2>Presentation of games and projects under Unreal Engine</h2>
        <em>GACKEL Tanguy</em>
        <p>Many companies use the game engine created by Epic Games,
            the Unreal Engine 4. These games can be of any type, but are generally
            3-dimensional games, because the Unreal Engine has been especially optimized for this.</p>
    </article>
    <article>
        <h3>I) Games made with the Unreal Engine 4</h3>
        <h4>1) Fortnite Battle Royale (2017):</h4>
        <p>Fortnite is a game developed and edited by Epic Games with the Unreal Engine.
            It is a Battle Royal, i.e. a game where all the players are dropped into an
            arena, whose edges narrow as the game progresses, forcing the
            players to meet each other, and thus fight. The last survivor is the winner</p>
        <p>Creating such an ambitious game with their own game engine has
            allowed Epic Games to become aware of its shortcomings, so they have some
            taken advantage of the opportunity to optimize it, particularly in terms of server performance, but also
            for the display of distant objects, necessary in a shooting game.</p>
        <br/>
        <h4>2) Ark: Survival Evolved (2015)</h4>
        <p>Ark is a sandbox survival game edited by Studio Wildcard with
            the Unreal Engine. In this game, the player finds himself on an island populated by
            dinosaurs, and has to survive while making weapons, shelter, and so
            unlock new technologies. The game focuses on a vast world and
            realistic graphics, despite the presence of many bugs.</p>
        <p>Ark benefits from many "mods".
            created by the community, and a
            extension of the Unreal Engine has been specially created for this purpose. The Ark Dev Kit allows
            so to create "mods" in a simplified way by using a modified version of
            the Unreal Engine, even for people with a low level of programming skills.</p>
        <br/>
        <h4>3) Projet BLARI (2020)</h4>
        <p>Project BLARI is a game developed by the youtubeur MaxEstLa. It is about
            his second game, the first one having also been made with the Unreal Engine. It was
            helped the music and ambient sounds. Project BLARI is a 3 dimensional game made in 3 dimensions
            in the style of the old games on the PS1 console.</p>
        <p>This game shows that anyone can make a game with the Unreal Engine,
            even a freelancer, alone, and that learning the software is quite fast
            (MaxEstLa learned many of the features while he was developing
            his game).</p>
    </article>
    <article>
        <h3>II) Projects made with Unreal Engine 4</h3>
        <h4>1) The Mandalorian (2020)</h4>
        <p>The Unreal Engine is not only used to create video games. Thanks to
            with the ultra-realistic graphics allowed by this game engine, its use can
            also be diverted and used for other purposes.</p>
        <p>The Mandalorian is a television series set in the universe of Star
            Wars. The peculiarity of The Mandalorian is that the green backgrounds have no or very
            little used. Instead, the scenes were shot in rooms where the walls and walls were
            are composed of gigantic LED panels. The different scenes have been
            modeled with Unreal Engine 4, then displayed on the screens. This method has
            many advantages, including the fact that the result is directly visible by the
            actors and the director. Changes to the set can therefore be made in the middle of shooting.</p>
